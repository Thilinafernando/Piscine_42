void	ft_rev_int_tab(int *tab, int size)
{
	int	a;
	int	b;
	int	s;

	a = 0;
	b = size -1;
	while ((size / 2) > a)
	{
		s = tab[a];
		tab[a] = tab[b];
		tab[b] = s;
		a++;
		b--;
	}
}
