void	ft_ultimate_div_mod(int *a, int *b)
{
	int	at;
	int	bt;

	at = *a;
	bt = *b;
	if (!(bt == 0))
	{
		*a = (at / bt);
		*b = (at % bt);
	}
	else
	{
		return ;
	}
}
